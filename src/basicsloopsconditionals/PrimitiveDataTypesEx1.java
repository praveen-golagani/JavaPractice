package basicsloopsconditionals;

public class PrimitiveDataTypesEx1 {

	public static void main(String[] args) {
		
		
		int id = 101;
		long money = 10100042424116l;
		float passPercent = 85.05f;
		double temp = 75.458158756d;
		char grade = 'A';
		System.out.println("id : "+id);
		System.out.println("amount : "+money);
		System.out.println("pass: "+passPercent);
		System.out.println("temperature : "+temp);
		System.out.println("grade : "+grade);

	}

}
